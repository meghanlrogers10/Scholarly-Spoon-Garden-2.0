import type {
  AdvisingMilestone,
  AdvisingMilestoneName,
  AdvisingStudent,
  Committee,
  ServiceItem,
} from "../types";
import { advisingMilestoneOrder } from "./advisingMilestones";

function buildMilestones(
  overrides: Partial<Record<AdvisingMilestoneName, Partial<AdvisingMilestone>>>
): AdvisingMilestone[] {
  return advisingMilestoneOrder.map((name) => ({
    id: `milestone-${name.toLowerCase().replaceAll(" ", "-")}`,
    name,
    status: "not-started",
    ...overrides[name],
  }));
}

